// Funci√≥n para verificar el estado actual
function verificarEstadoSincronizacion() {
  console.log('\nüìä VERIFICANDO ESTADO ACTUAL...');
  
  const auth = JSON.parse(localStorage.getItem('smart-student-auth') || '{}');
  const users = JSON.parse(localStorage.getItem('smart-student-users') || '[]');
  const studentAssignments = JSON.parse(localStorage.getItem('smart-student-student-assignments') || '[]');
  const teacherAssignments = JSON.parse(localStorage.getItem('smart-student-teacher-assignments') || '[]');
  
  const currentUser = auth.user;
  
  console.log(`üë§ Usuario actual: ${currentUser?.displayName || 'No logueado'} (${currentUser?.role || 'N/A'})`);
  console.log(`üìä Estad√≠sticas:`);
  console.log(`   ‚Ä¢ Total usuarios: ${users.length}`);
  console.log(`   ‚Ä¢ Estudiantes: ${users.filter(u => u.role === 'student').length}`);
  console.log(`   ‚Ä¢ Profesores: ${users.filter(u => u.role === 'teacher').length}`);
  console.log(`   ‚Ä¢ Asignaciones estudiantes: ${studentAssignments.length}`);
  console.log(`   ‚Ä¢ Asignaciones profesores: ${teacherAssignments.length}`);
  
  if (currentUser?.role === 'teacher') {
    const estudiantesAsignados = users.filter(u => 
      u.role === 'student' && (
        u.assignedTeacher === currentUser.username ||
        (u.assignedTeachers && Object.values(u.assignedTeachers).includes(currentUser.username))
      )
    );
    
    console.log(`üéì Para profesor ${currentUser.username}:`);
    console.log(`   ‚Ä¢ Estudiantes asignados: ${estudiantesAsignados.length}`);
    estudiantesAsignados.forEach(e => {
      console.log(`     - ${e.displayName || e.username}`);
    });
  }
  
  return {
    currentUser,
    users,
    studentAssignments,
    teacherAssignments
  };
}

// Funci√≥n para probar eventos de sincronizaci√≥n
function probarEventosSincronizacion() {
  console.log('\nüîÑ PROBANDO EVENTOS DE SINCRONIZACI√ìN...');
  
  // Event listeners para verificar que los eventos se disparen
  const eventosDetectados = [];
  
  const detectarEvento = (eventName) => {
    return (event) => {
      eventosDetectados.push({ 
        evento: eventName, 
        timestamp: new Date().toISOString(),
        detail: event.detail 
      });
      console.log(`‚úÖ Detectado evento: ${eventName}`, event.detail);
    };
  };
  
  window.addEventListener('usersUpdated', detectarEvento('usersUpdated'));
  window.addEventListener('studentAssignmentsUpdated', detectarEvento('studentAssignmentsUpdated'));
  window.addEventListener('teacherAssignmentsChanged', detectarEvento('teacherAssignmentsChanged'));
  
  // Simular cambios
  console.log('üîÑ Simulando cambio en usuarios...');
  window.dispatchEvent(new CustomEvent('usersUpdated', {
    detail: { action: 'test', source: 'manual', timestamp: new Date().toISOString() }
  }));
  
  setTimeout(() => {
    console.log('üîÑ Simulando cambio en asignaciones de estudiantes...');
    window.dispatchEvent(new CustomEvent('studentAssignmentsUpdated', {
      detail: { action: 'test', source: 'manual', timestamp: new Date().toISOString() }
    }));
  }, 1000);
  
  setTimeout(() => {
    console.log('üîÑ Simulando cambio en asignaciones de profesores...');
    window.dispatchEvent(new CustomEvent('teacherAssignmentsChanged'));
  }, 2000);
  
  setTimeout(() => {
    console.log('üìã RESUMEN DE EVENTOS DETECTADOS:');
    if (eventosDetectados.length === 0) {
      console.log('‚ùå No se detectaron eventos. La sincronizaci√≥n puede no estar funcionando.');
    } else {
      eventosDetectados.forEach((evento, index) => {
        console.log(`${index + 1}. ${evento.evento} - ${evento.timestamp}`);
      });
      console.log('‚úÖ Eventos detectados correctamente. La sincronizaci√≥n est√° funcionando.');
    }
  }, 3500);
}

// Funci√≥n para crear datos de prueba si no existen
function crearDatosPrueba() {
  console.log('\nüèóÔ∏è CREANDO DATOS DE PRUEBA...');
  
  const users = JSON.parse(localStorage.getItem('smart-student-users') || '[]');
  const auth = JSON.parse(localStorage.getItem('smart-student-auth') || '{}');
  
  if (!auth.user) {
    console.log('‚ùå No hay usuario logueado. Por favor, haz login primero.');
    return;
  }
  
  if (auth.user.role !== 'teacher' && auth.user.role !== 'admin') {
    console.log('‚ùå Necesitas estar logueado como profesor o admin para crear datos de prueba.');
    return;
  }
  
  // Crear estudiantes de prueba si no existen
  const estudiantesPrueba = ['ana', 'carlos', 'lucia'];
  let cambios = false;
  
  estudiantesPrueba.forEach(username => {
    const existeEstudiante = users.find(u => u.username === username);
    
    if (!existeEstudiante) {
      const nuevoEstudiante = {
        id: `student-${Date.now()}-${Math.random().toString(36).substr(2, 9)}`,
        username: username,
        name: username.charAt(0).toUpperCase() + username.slice(1),
        displayName: username.charAt(0).toUpperCase() + username.slice(1) + ' Test',
        email: `${username}@test.com`,
        role: 'student',
        password: '1234',
        activeCourses: ['4to B√°sico'],
        assignedTeacher: auth.user.role === 'teacher' ? auth.user.username : 'profesor_test',
        assignedTeachers: {
          'Matem√°ticas': auth.user.role === 'teacher' ? auth.user.username : 'profesor_test',
          'Lenguaje y Comunicaci√≥n': auth.user.role === 'teacher' ? auth.user.username : 'profesor_test',
          'Ciencias Naturales': auth.user.role === 'teacher' ? auth.user.username : 'profesor_test',
          'Historia, Geograf√≠a y Ciencias Sociales': auth.user.role === 'teacher' ? auth.user.username : 'profesor_test'
        }
      };
      
      users.push(nuevoEstudiante);
      cambios = true;
      console.log(`‚úÖ Creado estudiante: ${nuevoEstudiante.displayName}`);
    } else {
      console.log(`‚ÑπÔ∏è Estudiante ${username} ya existe`);
    }
  });
  
  if (cambios) {
    localStorage.setItem('smart-student-users', JSON.stringify(users));
    
    // Disparar eventos de sincronizaci√≥n
    window.dispatchEvent(new CustomEvent('usersUpdated', {
      detail: { action: 'create', source: 'test-data', timestamp: new Date().toISOString() }
    }));
    
    window.dispatchEvent(new CustomEvent('studentAssignmentsUpdated', {
      detail: { action: 'create', source: 'test-data', timestamp: new Date().toISOString() }
    }));
    
    console.log('‚úÖ Datos de prueba creados y eventos disparados');
  } else {
    console.log('‚ÑπÔ∏è No se crearon nuevos datos - ya existen estudiantes de prueba');
  }
}

// Funci√≥n para simular cambio en gesti√≥n de usuarios
function simularCambioEnGestionUsuarios() {
  console.log('\nüîÑ SIMULANDO CAMBIO EN GESTI√ìN DE USUARIOS...');
  
  const users = JSON.parse(localStorage.getItem('smart-student-users') || '[]');
  const auth = JSON.parse(localStorage.getItem('smart-student-auth') || '{}');
  
  if (!auth.user || auth.user.role !== 'teacher') {
    console.log('‚ùå Necesitas estar logueado como profesor para esta prueba');
    return;
  }
  
  // Buscar un estudiante para modificar
  const estudiante = users.find(u => 
    u.role === 'student' && 
    (u.assignedTeacher === auth.user.username || 
     (u.assignedTeachers && Object.values(u.assignedTeachers).includes(auth.user.username)))
  );
  
  if (!estudiante) {
    console.log('‚ùå No se encontr√≥ ning√∫n estudiante asignado. Ejecuta crearDatosPrueba() primero.');
    return;
  }
  
  console.log(`üîÑ Modificando estudiante: ${estudiante.displayName}`);
  
  // Simular cambio (agregar timestamp a displayName)
  const timestamp = new Date().toLocaleTimeString();
  estudiante.displayName = `${estudiante.name} (Actualizado ${timestamp})`;
  
  // Guardar cambios
  localStorage.setItem('smart-student-users', JSON.stringify(users));
  
  // Disparar eventos de sincronizaci√≥n
  window.dispatchEvent(new CustomEvent('usersUpdated', {
    detail: { 
      action: 'update', 
      userType: 'student',
      source: 'simulated-change',
      timestamp: new Date().toISOString() 
    }
  }));
  
  window.dispatchEvent(new CustomEvent('studentAssignmentsUpdated', {
    detail: { 
      action: 'update', 
      source: 'simulated-change',
      timestamp: new Date().toISOString() 
    }
  }));
  
  console.log('‚úÖ Cambio simulado y eventos disparados');
  console.log('üí° Ve a la pesta√±a Tareas y crea una nueva tarea para ver los cambios');
}

